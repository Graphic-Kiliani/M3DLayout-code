"""
run_pipeline_infinigen.py
Standalone Blender-headless pipeline for curating a single Infinigen seed scene.

Usage (via shell wrapper — recommended):
    bash run_pipeline_infinigen.sh /path/to/seed_folder

Usage (direct):
    blender --background --factory-startup --python run_pipeline_infinigen.py -- /path/to/seed_folder

Input:
    A seed folder generated by Infinigen, containing:
        <seed>/fine/scene.blend
        <seed>/coarse/solve_state.json

Output:
    <seed>/output/
        parameters_<seed>.json
        all_<seed>.json
        render_topdown_<seed>.png
        render_topdown_annotated_<seed>.png
        main_objects/render_main_objects_<seed>.png  (+ per-object views)
        render_oblique_<seed>_oblique_0..3.png
        floormask_<seed>.png
        description/description_topdown.txt          (requires OpenAI API key)
"""

