# Name of the .env file (looked up relative to this script's directory)
_ENV_FILE_NAME = ".env"


# =============================================================================
# Timing helpers
# =============================================================================
_pipeline_start = time.time()
_step_timings = []


def _fmt_elapsed(seconds):
    m, s = divmod(int(seconds), 60)
    return f"{m}m {s}s" if m > 0 else f"{s}s"


def _print_step_header(step_num, total, title, details=None):
    elapsed = time.time() - _pipeline_start
    print(f"\n{'='*60}")
    print(f"[Step {step_num}/{total}] {title}  (elapsed: {_fmt_elapsed(elapsed)})")
    if details:
        for line in details:
            print(f"  {line}")
    print(f"{'='*60}")
    sys.stdout.flush()


def _print_step_done(step_num, title, start_time, extra_msg=""):
    elapsed = time.time() - start_time
    _step_timings.append((step_num, title, elapsed))
    msg = f"[Step {step_num} Done] {title} — {_fmt_elapsed(elapsed)}"
    if extra_msg:
        msg += f"  {extra_msg}"
    print(msg)
    sys.stdout.flush()


